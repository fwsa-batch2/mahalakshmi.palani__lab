when"tuesday"   
    puts 'Wear Red or Orange'   
when "wednesday"   
    puts 'Wear Green'   
when "thursday"   
    puts 'Wear Yellow'   
when "friday"   
    puts 'Wear White'   
when "saturday"   
    puts 'Wear Black'   
else   
    puts "Wear Any color"   

end   